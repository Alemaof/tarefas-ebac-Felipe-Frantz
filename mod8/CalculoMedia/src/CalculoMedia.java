public class CalculoMedia {
    public static void main(String[] args) {

        double nota1 = 10;
        double nota2 = 8.5;
        double nota3 = 9;
        double nota4 = 5;
        double media;

        media = (nota1 + nota2 + nota3 + nota4) / 4;

        System.out.println("A media e : " + media);
    }
}
